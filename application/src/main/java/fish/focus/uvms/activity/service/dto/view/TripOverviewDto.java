package fish.focus.uvms.activity.service.dto.view;

import javax.json.bind.annotation.JsonbProperty;
import java.util.Date;
import java.util.List;

public class TripOverviewDto {

    private List<TripIdDto> tripId;

    @JsonbProperty(value = "tripTypeCode")
    private String typeCode;

    private Date departureTime;

    private Date arrivalTime;

    private Date landingTime;

    public List<TripIdDto> getTripId() {
        return tripId;
    }

    public void setTripId(List<TripIdDto> tripId) {
        this.tripId = tripId;
    }

    public Date getDepartureTime() {
        return departureTime;
    }

    public void setDepartureTime(Date departureTime) {
        this.departureTime = departureTime;
    }

    public Date getArrivalTime() {
        return arrivalTime;
    }

    public void setArrivalTime(Date arrivalTime) {
        this.arrivalTime = arrivalTime;
    }

    public Date getLandingTime() {
        return landingTime;
    }

    public void setLandingTime(Date landingTime) {
        this.landingTime = landingTime;
    }

    public String getTypeCode() {
        return typeCode;
    }

    public void setTypeCode(String typeCode) {
        this.typeCode = typeCode;
    }
}
